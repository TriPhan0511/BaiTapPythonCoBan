"""
Trả lời: 
Nếu x là số nguyên thì giá trị của hai biểu thức x ** 2.0 và x * x là khác nhau.
Nếu x là số thập phân thì giá trị của hai biểu thức x ** 2.0 và x * x là đồng nhất.
"""

x = 3
print(x ** 2.0)  # 9.0
print(x * x)  # 9

x = 3.14
print(x ** 2.0)  # 9.8596
print(x * x)  # 9.8596










